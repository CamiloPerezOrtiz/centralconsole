	name VARCHAR(25) NOT NULL, 
	email VARCHAR(50) NOT NULL, 
	password VARCHAR(255) NOT NULL, 
	role VARCHAR(15) NOT NULL, 
	nameGroup VARCHAR(25) NOT NULL, 
