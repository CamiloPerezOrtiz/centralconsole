	urlList VARCHAR(255) NOT NULL, 
	regularExpression VARCHAR(255) NOT NULL, 
	redirect VARCHAR(255) NOT NULL, 
	description VARCHAR(255) NOT NULL, 
	nameGroup VARCHAR(25) NOT NULL, 
	redirectMode VARCHAR(25) NOT NULL, 
	PRIMARY KEY(id)
);

